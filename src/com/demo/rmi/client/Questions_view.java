    /***
     * This method is for Auto increment for the Customer
     * It will execute according to the size of user_id field in the Database
     * when there is no user it will get assigned to 1
     * if there is already user exists it will take the last user(MAX) id from database and add 1 to the user
     */
    int quizno;
    String q;
    public void Auto_Increment() {

        try {
            Statement stat = con.createStatement();
            rst = stat.executeQuery("SELECT MAX(user_id) from answers");
            rst.next();
            rst.getString("MAX(user_id)");

            if (rst.getString("MAX(user_id)") == null) {

                txtuserid.setText("1");

            } else {

                quizno = Integer.parseInt(rst.getString("MAX(user_id)")) + 1;
                q = Integer.toString(quizno);
                txtuserid.setText(q);
                

            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
