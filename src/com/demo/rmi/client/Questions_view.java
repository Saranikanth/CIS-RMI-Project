    /***
     * This event is used to get the answer from the user and set it to the database for the Analysis
     * It will call the RetriveQuestions() method
     * if the question reached 10, it will stop Questions and direct to Dashboard
     * @param evt 
     */
    private void btnnextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnextActionPerformed
        // TODO add your handling code here:
       
         try {

            String answer = null;

         
                txtno.setText(rst.getString(1));
                txtquestion.setText(rst.getString(2));
                btnoption1.setText(rst.getString(3));
                btnoption2.setText(rst.getString(4));
                btnoption3.setText(rst.getString(5));

                if (btnoption1.isSelected()) {

                    answer = rst.getString(3);

                } else if (btnoption2.isSelected()) {

                    answer = rst.getString(4);

                } else if (btnoption3.isSelected()) {

                    answer = rst.getString(5);
                }
                
                String q = txtuserid.getText();
                int user = Integer.parseInt(q);
                int a = Integer.parseInt(txtno.getText());
                int b = a - 1;

                String sql = "INSERT INTO answers(user_id, question_id, answer) VALUES (?,?,?)";
                pst = con.prepareStatement(sql);

                pst.setInt(1, user);
                pst.setInt(2, a);
                pst.setString(3, answer);

                pst.executeUpdate();
            

             
        } catch (Exception ex) {

            ex.printStackTrace();

        }
        number++;
        buttonGroup1.clearSelection();
        
        if(number>10){
        JOptionPane.showMessageDialog(this,"Thank you for your feedback");
        dispose();
        Dashboard dashboard = new Dashboard();
        dashboard.setVisible(true);
        }
        if(number==10){
        btnnext.setText("Finish");
        }
        
        if(number<=10){
        
            RetriveQuestions();
        }
        
                                          
    }//GEN-LAST:event_btnnextActionPerformed

